package com.avl.datastructure;

public class Array {

    /**
     * 小技巧：直接输入下面
     * psvm ->     public static void main(String[] args) {}
     * sout ->     System.out.printf("");
     *
     *
     **/
    //直接输入 psvm 快捷创建
    public static void main(String[] args)
    {
        System.out.printf("");
        System.out.println("args = [" + args + "]");
    }
}
